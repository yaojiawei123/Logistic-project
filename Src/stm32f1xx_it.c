/**
  * @brief This function handles USART1 global interrupt.
  */
void USART1_IRQHandler(void)
{
  /* USER CODE BEGIN USART1_IRQn 0 */
  uint8_t Res;
	if(__HAL_UART_GET_FLAG(&huart1,UART_FLAG_RXNE) != 0x00u)
	{
	  Res = USART1 ->DR;
		My_Usart1.Usart1ReadBuff[My_Usart1.Usart1ReadCnt] = Res;
		My_Usart1.Usart1ReadCnt++;
		if(My_Usart1.Usart1ReadCnt > Usart1ReadBuffLen - 10){
		  My_Usart1.Usart1ReadCnt = 0;
		}
	}
	else if(__HAL_UART_GET_FLAG(&huart1,UART_FLAG_IDLE) != 0x00u)
	{
	  __HAL_UART_CLEAR_IDLEFLAG(&huart1);
		My_Usart1.Usart1ReadCnt = 0;
		My_Usart1.USART1_IT_IDLE = 1;
	}
  /* USER CODE END USART1_IRQn 0 */
  HAL_UART_IRQHandler(&huart1);
  /* USER CODE BEGIN USART1_IRQn 1 */

  /* USER CODE END USART1_IRQn 1 */
}

