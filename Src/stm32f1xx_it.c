/**
  * @brief This function handles UART4 global interrupt.
  */
void UART4_IRQHandler(void)
{
  /* USER CODE BEGIN UART4_IRQn 0 */
  uint8_t Res;
	if(__HAL_UART_GET_FLAG(&huart4,UART_FLAG_RXNE) != 0x00u)
	{
	  Res = UART4 ->DR;
		if(Res == '$')
		{
		  My_Uart4.Usart1ReadCnt = 0;
		}
		My_Uart4.Uart4ReadBuff[My_Uart4.Usart1ReadCnt++] = Res;
		if(My_Uart4.Uart4ReadBuff[0] == '$' && My_Uart4.Uart4ReadBuff[4] == 'M' && My_Uart4.Uart4ReadBuff[5] == 'C')
		{
	    if(Res == '\n')
			{
			  memset(Save_Data.GPS_Buffer,0,GPS_Buffer_Length);
				memcpy(Save_Data.GPS_Buffer,My_Uart4.Uart4ReadBuff,My_Uart4.Usart1ReadCnt);
				Save_Data.isGetData = TRUE;
				My_Uart4.Usart1ReadCnt = 0;
				memset(My_Uart4.Uart4ReadBuff,0,Uart4ReadBuffLen);
			}
		}
		if(My_Uart4.Usart1ReadCnt >= Uart4ReadBuffLen)
		{
		  My_Uart4.Usart1ReadCnt = Uart4ReadBuffLen;
		}
	}
  /* USER CODE END UART4_IRQn 0 */
  HAL_UART_IRQHandler(&huart4);
  /* USER CODE BEGIN UART4_IRQn 1 */

  /* USER CODE END UART4_IRQn 1 */
}

